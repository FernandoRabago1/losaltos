export function getTranslations(locale: Locale) {
  return translations[locale] || translations.es;
}

export function translateTypology(typology: string, locale: Locale): string {
  const t = translations[locale];
  const key = typology as keyof typeof t.typology;
  return t.typology[key] || typology;
}

export function translateStatus(status: string, locale: Locale): string {
  const t = translations[locale];
  const key = status as keyof typeof t.status;
  return t.status[key] || status;
}

export function translateTag(tag: string, locale: Locale): string {
  const t = translations[locale];
  const key = tag as keyof typeof t.tags;
  return t.tags[key] || tag;
}
